package anonymous.java;



public class twoD {
    public static void main(String[] args) {

        int[][] arr = {
            {23,43,54,-65,78},
            {42,-456,312},
            {21,1,3,-65,66,32}
        };


        // Printing the 2D array
        for (int[] array : arr) {
            for (int a : array) {
                System.out.println(a);
            }
        } 
    




    }
}
